const parametros = new URLSearchParams(window.location.search);
const categoriaActual = parametros.get("categoria");

const trabajo = trabajos[categoriaActual] || trabajos.conciertos;

document.title = `${trabajo.titulo} | NeoPulsor`;

document.getElementById("trabajo_categoria").textContent = trabajo.categoria;
document.getElementById("trabajo_titulo").textContent = trabajo.titulo;
document.getElementById("trabajo_descripcion").textContent =
  trabajo.descripcion;

const contenedorVideos = document.getElementById("trabajo_videos");
const contenedorGaleria = document.getElementById("trabajo_galeria");

contenedorVideos.innerHTML = "";
contenedorGaleria.innerHTML = "";

trabajo.videos.forEach((videoId) => {
  const tarjetaVideo = document.createElement("button");
  tarjetaVideo.classList.add("trabajo_video");
  tarjetaVideo.type = "button";
  tarjetaVideo.setAttribute(
    "aria-label",
    `Reproducir vídeo de ${trabajo.titulo}`,
  );

  tarjetaVideo.innerHTML = `
    <img
      src="https://img.youtube.com/vi/${videoId}/hqdefault.jpg"
      alt="Miniatura de vídeo de ${trabajo.titulo}"
      loading="lazy"
    />
    <span class="trabajo_video__play">▶</span>
  `;

  tarjetaVideo.addEventListener("click", () => {
    const iframe = document.createElement("iframe");

    iframe.src = `https://www.youtube.com/embed/${videoId}?autoplay=1`;
    iframe.title = `Vídeo de ${trabajo.titulo}`;
    iframe.allow =
      "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share";
    iframe.allowFullscreen = true;

    tarjetaVideo.replaceWith(iframe);
  });

  contenedorVideos.appendChild(tarjetaVideo);
});

let indiceImagenActual = 0;

const lightbox = document.getElementById("lightbox");
const lightboxImagen = document.getElementById("lightbox_imagen");
const botonCerrarLightbox = document.getElementById("lightbox_cerrar");
const botonAnterior = document.getElementById("lightbox_anterior");
const botonSiguiente = document.getElementById("lightbox_siguiente");

function abrirLightbox(indice) {
  indiceImagenActual = indice;

  lightboxImagen.src = trabajo.fotos[indiceImagenActual];
  lightboxImagen.alt = `${trabajo.titulo} - fotografía ${indiceImagenActual + 1}`;

  lightbox.classList.add("activo");
  lightbox.setAttribute("aria-hidden", "false");

  document.body.style.overflow = "hidden";
}

function cerrarLightbox() {
  lightbox.classList.remove("activo");
  lightbox.setAttribute("aria-hidden", "true");

  lightboxImagen.src = "";
  document.body.style.overflow = "";
}

function mostrarImagenAnterior() {
  indiceImagenActual =
    indiceImagenActual === 0
      ? trabajo.fotos.length - 1
      : indiceImagenActual - 1;

  lightboxImagen.src = trabajo.fotos[indiceImagenActual];
  lightboxImagen.alt = `${trabajo.titulo} - fotografía ${indiceImagenActual + 1}`;
}

function mostrarImagenSiguiente() {
  indiceImagenActual =
    indiceImagenActual === trabajo.fotos.length - 1
      ? 0
      : indiceImagenActual + 1;

  lightboxImagen.src = trabajo.fotos[indiceImagenActual];
  lightboxImagen.alt = `${trabajo.titulo} - fotografía ${indiceImagenActual + 1}`;
}

trabajo.fotos.forEach((foto, index) => {
  const imagen = document.createElement("img");

  imagen.src = foto;
  imagen.alt = `${trabajo.titulo} - fotografía ${index + 1}`;

  imagen.addEventListener("click", () => {
    abrirLightbox(index);
  });

  contenedorGaleria.appendChild(imagen);
});

botonCerrarLightbox.addEventListener("click", cerrarLightbox);
botonAnterior.addEventListener("click", mostrarImagenAnterior);
botonSiguiente.addEventListener("click", mostrarImagenSiguiente);

lightbox.addEventListener("click", (evento) => {
  if (evento.target === lightbox) {
    cerrarLightbox();
  }
});

document.addEventListener("keydown", (evento) => {
  if (!lightbox.classList.contains("activo")) return;

  if (evento.key === "Escape") {
    cerrarLightbox();
  }

  if (evento.key === "ArrowLeft") {
    mostrarImagenAnterior();
  }

  if (evento.key === "ArrowRight") {
    mostrarImagenSiguiente();
  }
});
